package entities;

public class CicatrizAuxiliar {
	private boolean rosto;
	private boolean peito;
	private boolean costas;
	private boolean braco;
	private boolean perna;
	private boolean pe;
	public boolean isRosto() {
		return rosto;
	}
	public void setRosto(boolean rosto) {
		this.rosto = rosto;
	}
	public boolean isPeito() {
		return peito;
	}
	public void setPeito(boolean peito) {
		this.peito = peito;
	}
	public boolean isCostas() {
		return costas;
	}
	public void setCostas(boolean costas) {
		this.costas = costas;
	}
	public boolean isBraco() {
		return braco;
	}
	public void setBraco(boolean braco) {
		this.braco = braco;
	}
	public boolean isPerna() {
		return perna;
	}
	public void setPerna(boolean perna) {
		this.perna = perna;
	}
	public boolean isPe() {
		return pe;
	}
	public void setPe(boolean pe) {
		this.pe = pe;
	}
	
	
}
