package entities;

public class DentesAuxiliar {
	
	private boolean premolares;
	private boolean molares;
	private boolean siso;
	private boolean canino;
	private boolean incisivo;
	
	public boolean isPremolares() {
		return premolares;
	}
	public void setPremolares(boolean premolares) {
		this.premolares = premolares;
	}
	public boolean isMolares() {
		return molares;
	}
	public void setMolares(boolean molares) {
		this.molares = molares;
	}
	public boolean isSiso() {
		return siso;
	}
	public void setSiso(boolean siso) {
		this.siso = siso;
	}
	public boolean isCanino() {
		return canino;
	}
	public void setCanino(boolean canino) {
		this.canino = canino;
	}
	public boolean isIncisivo() {
		return incisivo;
	}
	public void setIncisivo(boolean incisivo) {
		this.incisivo = incisivo;
	}
	
		
}
